int numberOfTheDay;

Console.WriteLine("Какой сейчас день недели по порядку?");
numberOfTheDay = Convert.ToInt32(Console.ReadLine());

if (numberOfTheDay == 1)
{Console.WriteLine("Сегодня понедельник.");}

if (numberOfTheDay == 1)
{Console.WriteLine("Сегодня понедельник.");}
if (numberOfTheDay == 2)
{Console.WriteLine("Сегодня вторнки.");}
if (numberOfTheDay == 3)
{Console.WriteLine("Сегодня среда.");}
if (numberOfTheDay == 4)
{Console.WriteLine("Сегодня четверг.");}
if (numberOfTheDay == 5)
{Console.WriteLine("Сегодня пятница.");}
if (numberOfTheDay == 5)
{Console.WriteLine("Сегодня суббота.");}
if (numberOfTheDay == 7)
{Console.WriteLine("Сегодня воскресенье.");}
else 
{Console.WriteLine("Такого дня не существует.");}